package movies;

public class Movie {
    String name;
    String category;

    public Movie(String name, String category) {
        this.name = name;
        this.category = category;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCategory() {
        return name;
    }

    public void setCategory(String category) {
        this.category = category;
    }
}
